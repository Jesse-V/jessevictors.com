      <div class="description">
         <p>
            In my senior year at Utah State, I took a course in computer security. The field had intrigued me for several years before that, but it became particularly interesting in May 2013 when it exploded into a flurry of activity in the wake of the <a href="https://en.wikipedia.org/wiki/Edward_Snowden#Revelations">release of classified NSA documents</a> by Edward Snowden. While the open-source community has always taken security seriously, they were thrown into a flurry of activity following the revelations. It was in the midst of this that I entered the field.
         </p>
         <p>
            Following the conclusion of the computer security course, I decided that I wanted to continue it post-graduation. I decided to enter my Master's at Utah State under <a href="http://digital.cs.usu.edu/~mingli/">Dr. Ming Li</a>, the CS Department's security professor. In addition to my research, I follow cryptography and security news on a regular basis, and I try to familiarize myself with the latest security techniques. The field is so fascinating: from the <a href="https://en.wikipedia.org/wiki/Diffie-Hellman_Key_Exchange">Diffie-Hellman-Merkle key exchange</a> to the <a href="https://www.torproject.org/docs/hidden-services.html.en">Tor hidden service protocol</a>, there are many brilliant systems out there built by some very clever people. I have decided to basis my research in anonymous networks: systems that provide ordinary users with a high degree of privacy, security, and anonymity, such as mixnets, I2P, and Tor. I've learned a great deal through administrating six Tor relays (including two exits) and subscribing to Tor and security mailing lists.
         </p>
         <p>
            I don't consider myself an expert on computer or network security, not yet anyways. I'm learning more all the time, but I have run penetration tests against several systems for money, and I carry a flashdrive with Kali Linux and Tails OS on it around me at all times. I'll consider to work in the field, and I'd like to pursuit it professionally if I can.
