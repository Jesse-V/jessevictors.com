      <div class="title">Education Synopsis</div>
      <ul>
         <li>Graduated from Wasilla High School in 2010</li>
         <li>Transferred 21 Advanced Placement credits to Utah State University</li>
         <li>Earned a 3.5 GPA, placed on the Dean's List, and in Good Standing</li>
         <li>Bachelor's in Computer Science from USU, a semester ahead of schedule</li>
         <li>Midway through my Master's at USU in computer and network security</li>
      </ul>
   </div>
   <p>
      I attended Wasilla High School in my home state of Alaska. Although my house is closer and within the boundaries of Houston High, my parents and I decided that Wasilla offered a superior education, in particular Advanced Placement courses. In my second year I took an introductory course to computer science, and at the advice of the teacher was almost immediately moved me to the AP course. I knew that I was very interested in this field and it was one that I could pursuit throughout college.
   </p>
   <p>
      I graduated high school in 2010. I scored 1880 on my SAT, 28 on my ACT, and with several letters of recommendation, I was accepted to Utah State University on a tuition scholarship. I have always appreciated the beauty of the Utah landscape, as well as its culture. The university scores well nationally, is well known for its computer science programs, and leads the nation in funding for aerospace research and the number of student experiments actually sent out into space. Attending Utah State made a lot of sense, and I've been very happy with my choice. I'm proud to be a student there and a computer science graduate.
   </p>
   <p>
      I entered into the computer science courses with three years of Java development experience and algorithm development behind me, so I had little problem acing the CS courses. I chose the science emphasis in the program, which is more math-oriented than the other options. I also made it a policy to take more than the average of 15 credits per year; thanks to the 21 AP credits I transferred and my average of 16 or 17 credits/semester, I noticeably accelerated my education. My favorite subjects included biology, discrete mathematics, computer graphics, operating systems, and advanced algorithms. It was during my OS & Concurrency course that I fell in love with Linux and I've been hooked ever since.
   </p>
   <p>
      During my senior year I took a course in computer security and soon found the field particularly interesting. On my own time I learned more about the <a href="https://www.torproject.org/">Tor Project</a> and set up my own relays. I also began to more closely follow security topics and news, and dug into the details of popular cryptographic algorithms. I could see the explosion of the security field in the wake of Edward Snowden's leaks, and I still believe that this field will not disappear any time soon.
   </p>
   <p>
      I finished my Bachelor's of CS in December 2013, one semester ahead of schedule. I finished with a 3.5 GPA, several letters of recommendation, and a major new interest. I entered the Master's program at USU in computer and network security, which I'm now midway through. My research specialty is in anonymity networks, such as mixnets, P2P, and Tor. I am now poised to learn as much as I can and then enter the field. I'm interested in security consulting, computer programming, or Linux administration.
   </p>

