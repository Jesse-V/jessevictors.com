      <p>
         Several times during the summer the family and I take a trip down to Seldovia, Alaska. It's a small town of about 300 people, and it's only accessible by plane or by boat. We built a house across the bay from town, and although it's off the electrical grid and has no running water, it's quite a pleasant place to stay. The surrounding ocean and incoming tide into the lagoon provide good opportunities for fishing, although we often just like to relax and enjoy the view. We also have a nice boat that we take out on the water whenever we like.
      </p>
      <p>
         Seldovia, and I think our house and nearly family cabins in particular, are a great example of Alaskan independence. City-folk probably wouldn't understand why we would want to vacation in a house that has no running water, doesn't have television, and has little Internet to speak of, but we do enjoy the change. Our electricity comes from a large array of solar panels, we boil rain-water for cooking, and get our drinking water from across town. It's really nice to change from the modern commodities, get away from everything, and enjoy the surroundings.
      </p>
