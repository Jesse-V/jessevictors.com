   <div class="recItem">
      <p>
         I'm fortunate to live in a beautiful area of Alaska. My family's log house, built by hand in the 60s, is surrounded for miles in all direction. It's twenty feet from a large creek that slowly flows from a nearby lake to the ocean. We've thinned out the trees around our house so we have a large lawn, but the house is still very tucked away amongst the trees. I enjoy being outside and I really appreciate our surroundings.
      </p>
      <p>
         During the spring my family imports thousands of pounds of honeybees and beekeeping supplies for distribution to the beekeepers in South-central Alaska. My dad runs hives of his own, but we're the largest supplier of honeybees and beekeeping supplies in the state of Alaska. Beekeeping is challenging in Alaska due to the shorter flowering season, but like everywhere else it starts in the spring. Distributing out all the honeybees is quite a craziness in the springtime.
      </p>
      <p>
         During the summer, everything is fully thawed and the temperature usually reaches the high 70s or low 80s. It becomes a great time to go fishing or go on hikes. I'm usually away from school for the summer, so I get to enjoy this part of the year. assist my parents with the beekeeping business and help with the beehives. When I was younger I swam in the creek from time to time, but now I maintain our lawn and work on the footbridge that we use to cross the creek and follow the trail to my grandparent's house.
      </p>
