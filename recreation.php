      <p>
         In my spare time, I'm very grateful that I have the opportunity to enjoy the Alaskan woods around the house. My family runs <a href="https://www.alaskawildflowerhoney.com/">Alaska Wildflower Honey</a>, one of the largest beekeeping operations in the state of Alaska, and this occupies some of my time during the summer. We also can escape and take our boat down to Seldovia, Alaska, a small and remote town in the southern end of the Kenai Peninsula.
      </p>
      <p>
         When I'm not outside, I usually spend my spare time browsing around the Internet. I hang out on IRC, check in on forums, and follow Linux or security news blogs. Sometimes I turn on the television and watch Top Gear on BBCA, military documentaries, or other interesting shows. I watch shows on Netflix, or I read a book series.
      </p>
