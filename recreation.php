      <p>
         In my spare time, I enjoy watching BBC America, the Military Channel, or Netflix. War documentaries and Top Gear are among my favorite shows on television. On Netflix I've watched medical dramas, medieval movies, action adventures, and other popular shows. Movie-wise, I think the Lord of the Rings is one of the greatest films ever made. I enjoyed the books, including the Hobbit, but I was disappointed by its film adaptation.
      </p>
      <p>
         On television or on film, I enjoy sometimes focusing on the details of the production: the costumes, the cinematography, the details in the background, and the soundtrack. In this the Lord of the Rings excels, but other films do well in too. For particularly good soundtracks, I find them on YouTube and listen to them through my studio-quality headphones. I'm a casual audiophile hobbyist, and I have really come to appreciate the quality of well-delivered sound.
      </p>
